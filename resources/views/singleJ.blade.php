								<span class="image featured"><img src="images/pc2.jpg" alt="" /></span>
								<p>La evolución de los sistemas de información expone el desarrollo de estos métodos que cumplen la tarea de organizar y administrar recursos, motivo por el que muchos dicen que son tan antiguos como la civilización misma.</p>
								<p>El origen de estos sistemas lo podemos encontrar en Egipto aproximadamente en el 4.000 a.C. donde se realizó un censo de población, censo que se utilizó la recoger información y utilizarla para la toma de decisiones.

									Sin embargo, desde entonces los sistemas de información han ido evolucionando, y ya no se limitan solo a lo físico, sino que también forma una parte crucial de la informática.</p>
									<p>1950-1960</p>
									<p>
										No es secreto para nadie que durante la década de los 50’, los sistemas de información eran bastantes simples y primitivos, y esto se debía a la tecnología de la época.
										
										Por ello la principal función de dichos sistemas era asistir a los trabajadores de diferentes áreas ayudándoles en sus distintas tareas, siendo algunas de estas el procesar transacciones, mantener los registros y llevar la contabilidad básica.
										
										De esta forma, el SI (Sistema de información), se utilizaron principalmente en el procesamiento electrónico de datos. Se empleaban ordenadores para registrar, clasificar y manipular datos.  A esto se le puede llamar “sistemas de información de base de datos”.</p>
										<p>1980-1990</p>
										<p>Durante la década de los 80’ se desarrolló la informática departamental. Esto se dio gracias a que muchas empresas y organizaciones compraban su propio software y hardware con el fin de satisfacer sus necesidades departamentales.

											Esto hizo que cada departamento to tuviera que esperar el apoyo indirecto del departamento de servicio corporativo centralizado. La informática departamental les permitió a los empleados poder emplear sus propios recursos con la finalidad de respaldar sus requisitos laborales.
											
											Sin embargo, no todo fue color de rosas, ya que este crecimiento trajo consigo nuevos desafíos de compatibilidad de datos, integridad y conectividad de diferentes departamentos.</p>

											<p>1990-2000</p>
											<p>En este punto llegamos a hablar de la evolución de los sistemas de información en los últimos 30 años. En la década de 1980 surgió lo que se conoce como la “Informática departamental”. La cual consiste en que cada departamento de una organización se encargaba de comprar el hardware y software necesario para cubrir sus propias necesidades.

												Durante esa era, las intranet, extranet e internet, cambiaron significativamente los sistemas de información aplicados a los negocios. Fue allí cuando se pudo hacer posible circular el conocimiento hasta cualquier parte del mundo, independientemente del tiempo y el espacio.
												
												Fue también durante ese periodo que el mundo presenció la aparición de ERP (sistemas de planificación de recursos empresariales). Los ERP incorporan todo los componentes de una organización: Marketing, fabricación, ventas, gestión de recursos y planificación de recursos humanos.</p>
												<p>2000-presente</p>
												<p>Y ahora llegamos hasta la década 2000. Una era crucial en el desarrollo tecnológico, y por consiguiente en el informativo. A partir del 2000 se comenzaron a gestar nuevos softwares, hardware y nuevas formas de negocio que derivaron de las mejoras tecnológicas.

													El internet y la tecnología cambiaron la forma en que las empresas y las personas trabajan. Sin embargo, las funciones de los Sistemas de información de esta década siguen siendo las mismas que hace 40 o 50 años. Siguen realizando mantenimiento de registros, gestión de informes, procesamiento de transacciones, gestión de organización y gestión de soporte.</p>
